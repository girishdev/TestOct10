GitHub:
   Bug tracking
   Find your project easily
   Meet new developers
   Share your experience
   Coordinate your projects together
   Integrated code search
   High compatibility

GitHub and version control features: working with repositories, inspecting diffs, doing code review / pull requests, …

GitLab:
   GitLab exists in two forms.
      - The first one is SAAS
      - Individual solution GitLab Community Edition.
   Free service
   Open source code repository
   Free hosting
   Bug tracking mechanism
   Files editing in the web interface
   LDAP integration (Lightweight Directory Access Protocol)

GitLab Offers a whole “platform” around code: continuous integration / deployments / devops, more advanced issue management, …

GitLab benefits: reasons to use GitLab
   Free service
   Open source code repository
   Free hosting
   Bug tracking mechanism















